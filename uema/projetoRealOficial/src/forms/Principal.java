    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Cliente client = (Cliente) cmbCliente.getSelectedItem();
        Notebook nb = (Notebook) cmbNotebook.getSelectedItem();
        
        venda.setDesconto(nb.getPreco()*0.1);
        venda.setTotal(nb.getPreco()-venda.getDesconto());
        venda.setDataVenda(jFormattedTextField3.getText());
        note.setEstoque(nb.getEstoque()-1);
        
        double n = nb.getPreco();
        jTextField7.setText(String.valueOf(n-n*0.1));
        jTextField8.setText(String.valueOf(venda.getDesconto()));
        
        if(nb.getEstoque()<=0){
            JOptionPane.showMessageDialog(null, "Desculpe. No momento, esse notebook não está disponível no estoque!");
        }else{
            int r =  JOptionPane.showConfirmDialog(null, "Confirme os dados:\nNome do cliente: "+client.getNome()+
                    "\nNotebook: "+nb.getMarca()+" - "+nb.getModelo()+"\nData: "+venda.getDataVenda()+
                    "\nPreço total: "+venda.getTotal()+"\nDesconto: "+venda.getDesconto());

            if(r==JOptionPane.YES_OPTION){
                try {
                // TODO add your handling code here:
                    PreparedStatement pst = conex.conn.prepareStatement("insert into venda(dataVenda,total,desconto,Notebook_idNotebook,Cliente_idCliente,cli,note)values(?,?,?,?,?,?,?)");
                    pst.setString(1, venda.getDataVenda());
                    pst.setDouble(2, venda.getTotal());
                    pst.setDouble(3, venda.getDesconto());
                    pst.setInt(4, 1/*client.getIdCliente()*/);
                    pst.setInt(5, 1/*nb.getIdNotebook()*/);
                    pst.setString(6, client.getNome());
                    pst.setString(7, nb.getMarca() + " - " + nb.getModelo());
                    pst.executeUpdate();
                    PreparedStatement pst2 = conex.conn.prepareStatement("UPDATE notebook SET estoque=? WHERE idNotebook=?");
                    pst2.setInt(1, note.getEstoque());
                    pst2.setDouble(2, nb.getIdNotebook());
                    pst2.executeUpdate();
                    System.out.println("Salvo com sucesso!");
                } catch (SQLException ex) {
                    System.out.println("Erro na inserção!\nErro: "+ex);
                }
                JOptionPane.showMessageDialog(null, "Registrado com Sucesso!");
                preencherComboNotebook();
                cmbCliente.setSelectedItem(null);
                cmbNotebook.setSelectedItem(null);
                jFormattedTextField3.setText("");
            }
            jTextField7.setText("");
            jTextField8.setText("");
        }
        preencherTabela3("select idVenda,cli,note,dataVenda,desconto,total from venda order by idVenda");
        preencherTabela2("select * from notebook order by idNotebook");
    }//GEN-LAST:event_jButton5ActionPerformed

