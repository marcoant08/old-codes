    public void preencherComboCliente(){
        ArrayList<Cliente> listaClientes = conex.buscarCliente();
        
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) cmbCliente.getModel();
        modelo.removeAllElements();
        
        for(Cliente c : listaClientes){
            modelo.addElement(c);
        }
        
        cmbCliente.setSelectedIndex(-1);
    }
    
